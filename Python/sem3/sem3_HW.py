

def ex1a():
    from random import randrange
    nums = [randrange(1, 20) for i in range(randrange(3, 15))]  # Создаем список чисел случайного размера заполненный случайными числами
    result = []                                                 # Пустой список в который будут складываться результаты
    left = 0                                                    # Левое число произведения
    right = len(nums) -1                                        # Правое число произведения
    while left <= right:                                        # Пока числа не встретились
        result.append(nums[left] * nums[right])                 # Перемножаем и добавляем в результирующий список 
        left += 1                                               # Переходим на следующее число слева
        right -= 1                                              # Переходим на предыдущее число справа
    print('Исходный список:', nums)    
    print("Произведения:", result)


def ex1b():
    from random import randrange
    nums = [randrange(1, 20) for i in range(randrange(3, 15))]  # Создаем список чисел случайного размера заполненный случайными числами
    result = []                                  # Пустой список в который будут складываться результаты
    for i in range((len(nums)+1)//2):            # Проходим половину списка                                       
        result.append(nums[i] * nums[-(i+1)])    # Умножаем элемент на "противоположный" и добавляем результат в результирующий список
    print('Исходный список:', nums)    
    print("Произведения:", result)


def ex2():
    from random import randrange, uniform
    nums = [round(uniform(1, 100), 2) for i in range(randrange(3, 15))]     # Создаем список чисел случайного размера заполненный случайными вещественными числами
    rounded_nums = [round(i%1, 2) for i in nums]                            # Избавляемся от целой части
    print("Исходный список:", nums)
    print('Разница между максимальными и минимальными сотыми:', round(max(rounded_nums) - min(rounded_nums), 2))  # Находим min и max, выводим разность

def ex3a():
    num = int(input('Введите число >>> '))
    result = ""
    while num > 0:
        temp = num % 2                      # Берем остаток от деления на 2
        result = str(temp) + result         # Добавляем в результат в конец строки
        num //= 2                           # Делим исходное число на 2
    print('Число в двоичной системе счисления:', result)


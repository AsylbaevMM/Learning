def ex4():
    fibs = [0 for i in range(int(input('Введите число >>> '))+1)]  # Создаем список длиной равной заданному числу + 1 заполненный нулями 
    fibs[1] = 1                                # Присваиваем второму элементу значение "1" 
    for i in range(2, len(fibs)):              # проходимся по элементам списка начиная с тртьего заполняя последовательность Фибоначчи
        fibs[i] = fibs[i-1] + fibs[i-2]
    neg_fibs = []                              # создаем пустой список
    for i in range(len(fibs)):                 # наполняем его последовательностью Фибоначчи для отрицательных чисел
        neg_fibs.append(fibs[i]*(-1)**(i+1))   
    print(neg_fibs[::-1] + fibs[1:])           # при помощи срезов убираем лишний ноль, разворачиваем отрицательную последовательность и выводим результат


